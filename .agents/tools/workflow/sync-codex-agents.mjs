#!/usr/bin/env node

import { existsSync, mkdirSync, readdirSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import { basename, dirname, join, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const GENERATED_MARKER = "# Generated by .agents/tools/workflow/sync-codex-agents.mjs";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(scriptDir, "../../..");
const sourceRoot = resolve(repoRoot, ".claude/agents");
const outputRoot = resolve(repoRoot, ".codex/agents");

function usage() {
  console.log(`Usage: node .agents/tools/workflow/sync-codex-agents.mjs [--check]

Generates .codex/agents/*.toml from .claude/agents/**/*.md.

Options:
  --check   Verify generated files are current without writing changes.`);
}

function parseArgs(argv) {
  const args = { check: false };
  for (const arg of argv) {
    if (arg === "--check") {
      args.check = true;
    } else if (arg === "--help" || arg === "-h") {
      usage();
      process.exit(0);
    } else {
      throw new Error(`Unknown argument: ${arg}`);
    }
  }
  return args;
}

function listMarkdownFiles(dir) {
  if (!existsSync(dir)) return [];
  const entries = readdirSync(dir, { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    const path = join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...listMarkdownFiles(path));
    } else if (entry.isFile() && entry.name.endsWith(".md")) {
      files.push(path);
    }
  }
  return files.sort();
}

function splitFrontmatter(text, filePath) {
  const match = text.match(/^---\r?\n([\s\S]*?)\r?\n---\r?\n?/);
  if (!match) {
    throw new Error(`${filePath} is missing YAML frontmatter`);
  }
  return {
    frontmatter: match[1],
    body: text.slice(match[0].length).replace(/^\r?\n/, ""),
  };
}

function stripQuotes(value) {
  const trimmed = value.trim();
  if (
    (trimmed.startsWith('"') && trimmed.endsWith('"')) ||
    (trimmed.startsWith("'") && trimmed.endsWith("'"))
  ) {
    return trimmed.slice(1, -1);
  }
  return trimmed;
}

function removeCommonIndent(lines) {
  const nonBlank = lines.filter((line) => line.trim().length > 0);
  if (nonBlank.length === 0) return lines.join("\n");
  const minIndent = Math.min(
    ...nonBlank.map((line) => {
      const match = line.match(/^\s*/);
      return match ? match[0].length : 0;
    })
  );
  return lines.map((line) => line.slice(Math.min(minIndent, line.length))).join("\n");
}

function parseFrontmatter(frontmatter, filePath) {
  const lines = frontmatter.split(/\r?\n/);
  const values = {};

  for (let i = 0; i < lines.length; i += 1) {
    const line = lines[i];
    if (!line.trim()) continue;

    const match = line.match(/^([A-Za-z0-9_-]+):(?:\s*(.*))?$/);
    if (!match) {
      throw new Error(`${filePath} has unsupported frontmatter line: ${line}`);
    }

    const key = match[1];
    const rawValue = match[2] ?? "";
    if (rawValue === "|" || rawValue === ">" || rawValue === "") {
      const block = [];
      while (i + 1 < lines.length && (/^\s/.test(lines[i + 1]) || !lines[i + 1].trim())) {
        block.push(lines[i + 1]);
        i += 1;
      }
      const value = removeCommonIndent(block);
      values[key] = rawValue === ">" ? value.replace(/\n+/g, " ").trim() : value.trimEnd();
    } else {
      values[key] = stripQuotes(rawValue);
    }
  }

  return values;
}

function tomlString(value) {
  return JSON.stringify(String(value));
}

function tomlMultiline(value) {
  const normalized = String(value).replace(/\r\n/g, "\n").replace(/\r/g, "\n");
  const escaped = normalized.replace(/\\/g, "\\\\").replace(/"""/g, '\\"\\"\\"');
  return `"""\n${escaped}\n"""`;
}

function sanitizeFilename(name) {
  return name
    .trim()
    .replace(/[^A-Za-z0-9._-]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function convertAgent(filePath) {
  const raw = readFileSync(filePath, "utf8");
  const { frontmatter, body } = splitFrontmatter(raw, filePath);
  const meta = parseFrontmatter(frontmatter, filePath);
  const fallbackName = basename(filePath, ".md");
  const name = meta.name || fallbackName;
  const outputName = sanitizeFilename(name);
  if (!outputName) {
    throw new Error(`${filePath} produced an empty Codex agent filename`);
  }

  const description = meta.description || `Generated from ${relative(repoRoot, filePath)}`;
  const sourcePath = relative(repoRoot, filePath);
  const outputPath = join(outputRoot, `${outputName}.toml`);
  const lines = [
    GENERATED_MARKER,
    `# Source: ${sourcePath}`,
    "",
    `name = ${tomlString(name)}`,
    `description = ${tomlMultiline(description)}`,
    `developer_instructions = ${tomlMultiline(body.trimEnd())}`,
    "",
  ];

  if (meta.model && meta.model !== "inherit") {
    lines.splice(5, 0, `model = ${tomlString(meta.model)}`);
  }

  return {
    sourcePath,
    outputPath,
    content: lines.join("\n"),
  };
}

function listGeneratedCodexAgents(dir) {
  if (!existsSync(dir)) return [];
  return readdirSync(dir, { withFileTypes: true })
    .filter((entry) => entry.isFile() && entry.name.endsWith(".toml"))
    .map((entry) => join(dir, entry.name))
    .filter((filePath) => readFileSync(filePath, "utf8").startsWith(GENERATED_MARKER));
}

function main() {
  const { check } = parseArgs(process.argv.slice(2));
  const sources = listMarkdownFiles(sourceRoot);
  const converted = sources.map(convertAgent);
  const outputPaths = new Set();

  for (const agent of converted) {
    if (outputPaths.has(agent.outputPath)) {
      throw new Error(`Multiple Claude agents generate ${relative(repoRoot, agent.outputPath)}`);
    }
    outputPaths.add(agent.outputPath);
  }

  const staleGenerated = listGeneratedCodexAgents(outputRoot).filter((filePath) => !outputPaths.has(filePath));
  const changes = [];

  for (const agent of converted) {
    const current = existsSync(agent.outputPath) ? readFileSync(agent.outputPath, "utf8") : undefined;
    if (current !== agent.content) {
      if (current !== undefined && !current.startsWith(GENERATED_MARKER)) {
        throw new Error(
          `${relative(repoRoot, agent.outputPath)} already exists and is not generated by this script`
        );
      }
      changes.push(relative(repoRoot, agent.outputPath));
      if (!check) {
        mkdirSync(dirname(agent.outputPath), { recursive: true });
        writeFileSync(agent.outputPath, agent.content);
      }
    }
  }

  for (const filePath of staleGenerated) {
    changes.push(relative(repoRoot, filePath));
    if (!check) {
      rmSync(filePath);
    }
  }

  if (check && changes.length > 0) {
    console.error(`Codex agent sync is out of date:\n${changes.map((path) => `- ${path}`).join("\n")}`);
    process.exit(1);
  }

  const action = check ? "Checked" : "Synced";
  console.log(`${action} ${converted.length} Codex agent${converted.length === 1 ? "" : "s"}.`);
  for (const agent of converted) {
    console.log(`- ${agent.sourcePath} -> ${relative(repoRoot, agent.outputPath)}`);
  }
  for (const filePath of staleGenerated) {
    console.log(`- removed stale ${relative(repoRoot, filePath)}`);
  }
}

try {
  main();
} catch (error) {
  console.error(error instanceof Error ? error.message : String(error));
  process.exit(1);
}
